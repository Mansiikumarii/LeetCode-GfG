//  Implement Calculator class with all methods
class Calculator{
    public int sum(int a, int b){
        int sum = a+b;
        return sum;
    }
    public int sum(int a, int b, int c){
        int sum = a+b+c;
        return sum;
    }
    public double sum(double x, double y){
        double sum = x+y;
        return sum;
    }
}